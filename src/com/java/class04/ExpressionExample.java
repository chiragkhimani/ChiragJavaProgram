package com.java.class04;

public class ExpressionExample {

    public static void main(String args[]){
        System.out.println(10 * 4 / 8);

        System.out.println(4 * 10 - 3 * 20 + 45 / 3);

        System.out.println(50 / 10 * 5 * 10 / 2 + 5 * 4 / 8);

        System.out.println(10 * 20 / 100 - 20 * 4 / 2);

        System.out.println(150 / 15 * (3 + 2) * 2 / 10);

        System.out.println(120 / ( 15 * 4 - 2) + 11 / 6);
    }

}
